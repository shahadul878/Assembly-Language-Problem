.MODEL SMALL
.STACK 100H
.DATA
    
    NUM1 DB "Enter a Number : A= $"
    NUM2 DB ,0DH,0AH,"Enter a Number : B = $"
    ADD1 DB ,0DH,0AH," ADDITION(A+B) = $"
    SUB1 DB ,0DH,0AH,"SUBTRACTION(A-B) = $"
    MUL1 DB ,0DH,0AH,"MULTIPLICATION: ",0DH,0AH,'$'
    MUL2 DB ,0DH,0AH,"MULTIPLICATION (A*B) = $" 
    DIV1 DB ,0DH,0AH,"DIVISION :",0DH,0AH,'$'
    DIV2 DB ,0DH,0AH,"DIVISION (A/B) = $"
    EX DB ,0DH,0AH,"Thank You :)",0DH,0AH,'$'  
           
              
          OP1 DB ?
          OP2 DB ?
          Operand DB ?
          
.CODE


.STARTUP
START:

ADD: ; PERFORMING ADDITION

;FIRST OPERAND
MOV AH,09H
MOV DX,OFFSET NUM1
INT 21H
MOV AH,01H
INT 21H
MOV OP1,AL
;SECOND OPERAND
MOV AH,09H
MOV DX,OFFSET NUM2
INT 21H
MOV AH,01H
INT 21H
MOV OP2,AL
MOV AH,09H
MOV DX,OFFSET ADD1
INT 21H
MOV AL,OP1
MOV BL,OP2
ADD AL,BL
AAS
OR AX, 3030H
; PRINT RESULT
MOV AH,0EH
INT 10H




SUB: ; PERFORMING SUBTRACTION
MOV AH,09H
MOV DX,OFFSET SUB1
INT 21H 

MOV AL,OP1
MOV BL,OP2
SUB AL,BL
AAS
OR AX, 3030H
; PRINT RESULT
MOV AH,0EH
INT 10H

MUL: ; PERFORMING MULTIPLICATION
MOV AH,09H
MOV DX,OFFSET MUL1
INT 21H
;FIRST OPERAND
MOV AH,09H
MOV DX,OFFSET NUM1
INT 21H
MOV AH,01H
INT 21H
SUB AL,30H
MOV OP1,AL 
;SECOND OPERAND
MOV AH,09H
MOV DX,OFFSET NUM2
INT 21H
MOV AH,01H
INT 21H
SUB AL,30H
MOV OP2,AL
MOV AH,09H
MOV DX, OFFSET MUL2
INT 21H
MOV AL,OP1
MOV BL,OP2
MUL BL
ADD AL,30H
; PRINT RESULT
MOV AH,0EH
INT 10H



DIV: ; PERFORMING DIVISION
MOV AH,09H
MOV DX,OFFSET DIV1
INT 21H
;FIRST OPERAND
MOV AH,09H
MOV DX,OFFSET NUM1
INT 21H
MOV AH,01H
INT 21H 
SUB AL,30H
MOV OP1,AL
;SECOND OPERAND
MOV AH,09H
MOV DX,OFFSET NUM2
INT 21H
MOV AH,01H
INT 21H 
SUB AL,30H
MOV OP2,AL
MOV AH,09H
MOV DX, OFFSET DIV2
INT 21H
MOV AX,0000H
MOV AL,OP1
MOV BL,OP2
DIV BL
ADD AL,30H
; PRINT RESULT
MOV AH,0EH
INT 10H





EXIT:  ; SAY GOOD BYE AND THEN EXIT
MOV AH,09H
MOV DX,OFFSET EX
INT 21H
.EXIT
END















